    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        int selectedRow = jTable1.getSelectedRow();
        if(selectedRow == -1){
            JOptionPane.showMessageDialog(this, "Pilih baris yang akan diperbarui");
        return;
        }
        String id = jTable1.getValueAt(selectedRow, 0).toString();
        Date tanggal = new Date(dateAgenda.getDate().getTime());
        String namaKegiatan = tfKegiatan.getText();
        String keterangan = taKeteragan.getText();
        
         try {
        String sql = "UPDATE tb_agenda SET tanggal=?,nama_kegiatan=?,keterangan=? WHERE id=?";
        try (PreparedStatement st = Con.prepareStatement(sql)) {
            st.setDate(1, tanggal);
            st.setString(2, namaKegiatan);
            st.setString(3, keterangan);
            st.setString(4, id);

            int rowUpdate = st.executeUpdate();

            if (rowUpdate > 0) {
                JOptionPane.showMessageDialog(this, "Data Berhasil DiUbah");
                jButton3ActionPerformed(evt);
                tampilDataAgenda();
            } else {
                JOptionPane.showMessageDialog(this, "Gagal mengubah data.");
            }
        }
    } catch (Exception e) {
        JOptionPane.showMessageDialog(rootPane, "Gagal mengubah data " + e.getMessage());
    }
    }//GEN-LAST:event_jButton2ActionPerformed

