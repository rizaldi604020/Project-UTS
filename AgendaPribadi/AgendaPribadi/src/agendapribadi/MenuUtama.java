    //method menampilkan data dari database ke jtable
    private void tampilDataAgenda(){
        DefaultTableModel kolomkolom = new DefaultTableModel();
        kolomkolom.addColumn("No");
        kolomkolom.addColumn("Tanggal");
        kolomkolom.addColumn("Nama Kegiatan");
        kolomkolom.addColumn("Keterangan");
        
         
        
        try{
            int nomorOtomatis = 1;
            st=Con.createStatement();
            rs=st.executeQuery("SELECT*FROM tb_agenda");
            while(rs.next()){
                kolomkolom.addRow(new Object[]{
                    (""+nomorOtomatis++),
                    rs.getString(2),
                    rs.getString(3),
                    rs.getString(4)
                });
                jTable1.setModel(kolomkolom);
                dateAgenda.requestFocus();
            }
        } catch (Exception e){
            JOptionPane.showMessageDialog(null, "Gagal menampilkan Database \n"+e);
        }
        
    }

    
    
    
    
    

