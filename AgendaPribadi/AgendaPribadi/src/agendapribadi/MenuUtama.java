    private void itemHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_itemHapusActionPerformed
          int selectedRow = jTable1.getSelectedRow();

    if (selectedRow == -1) {
        JOptionPane.showMessageDialog(this, "Pilih data yang ingin dihapus.", "Peringatan", JOptionPane.WARNING_MESSAGE);
    } else {
        String namaKegiatanToDelete = (String) jTable1.getValueAt(selectedRow, 2); // Asumsi nama kegiatan ada di kolom ketiga

        // Tampilkan konfirmasi dialog
        int dialogResult = JOptionPane.showConfirmDialog(this, "Apakah Anda yakin ingin menghapus data?", "Konfirmasi Hapus", JOptionPane.YES_NO_OPTION);

        if (dialogResult == JOptionPane.YES_OPTION) {
            try {
                String sql = "DELETE FROM tb_agenda WHERE nama_kegiatan = ?";
                
                try (PreparedStatement st = Con.prepareStatement(sql)) {
                    st.setString(1, namaKegiatanToDelete);

                    int rowDeleted = st.executeUpdate();

                    if (rowDeleted > 0) {
                        JOptionPane.showMessageDialog(this, "Data berhasil dihapus.");
                        tampilDataAgenda();
                    } else {
                        JOptionPane.showMessageDialog(this, "Gagal menghapus data.");
                    }
                }
            } catch (SQLException e) {
                JOptionPane.showMessageDialog(rootPane, "Gagal menghapus data " + e.getMessage());
            }
        }
    }
    }//GEN-LAST:event_itemHapusActionPerformed

